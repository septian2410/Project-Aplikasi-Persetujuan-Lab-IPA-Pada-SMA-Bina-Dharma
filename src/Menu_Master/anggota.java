    private void BtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUpdateActionPerformed
       try {
            String id = TID.getText();
            String nama = TNM.getText();
            String user = TUSER.getText();
            String pass = TPASS.getText();
            Object lvl = TLEVEL.getSelectedItem();
            con.createStatement().executeUpdate("update user set "
                + "nm_lengkap='"+nama          +"', "
                + "username='"+user            +"', "
                + "password='"+pass            +"', "
                + "level='"+lvl                +"' "
                + "where id_user='"+model.getValueAt(tabel_user.getSelectedRow(),0)+"'");
            JOptionPane.showMessageDialog(this, "Data Berhasil Di Edit");
        }catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "Data Gagal Di Edit");
        }
        clear();
        tabelUser();
        BtnHapus.setEnabled(false);
        BtnUpdate.setEnabled(false);
    }//GEN-LAST:event_BtnUpdateActionPerformed

