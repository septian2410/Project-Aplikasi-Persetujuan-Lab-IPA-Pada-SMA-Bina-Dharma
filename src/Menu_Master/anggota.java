  private void tabelUser() {
        String[] judul = {"ID USER","Nama","Username","Password","Level"};
        model = new DefaultTableModel(judul,0);
        tabel_user.setModel(model);
        String sql = "SELECT * FROM user where id_user like '%"+cari.getText()+"%' or nm_lengkap like '%"+cari.getText()+"%'";
        try {
            rs = con.createStatement().executeQuery(sql);
            while(rs.next()) {
               String id = rs.getString("id_user");
               String nama = rs.getString("nm_lengkap");
               String user = rs.getString("username");
               String pass = rs.getString("password");
               String lvl = rs.getString("level");
               String[] data = {id,nama,pass,user,lvl};
               model.addRow(data);
           }
        }catch(Exception e) {
           System.out.println(e);
        }
    }
  
  
