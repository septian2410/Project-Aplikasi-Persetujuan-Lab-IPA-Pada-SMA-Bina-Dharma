    private void BtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUpdateActionPerformed
        String tampilan1 = "yyyy-MM-dd";
        SimpleDateFormat fm1 = new SimpleDateFormat(tampilan1);
        String tanggal1 = String.valueOf(fm1.format(TTGL_PRAKTIK.getDate()));

        try {
            String idpinjam = TID_PINJAM.getText();
            String kls = TKLS.getText();
            String praktik = TKEGIATAN.getText();
            String nama = TNM.getText();
            String jml = TJML.getText();
            Object JENIS = TJENIS.getSelectedItem();
            Object stat = TSTAT.getSelectedItem();
            Object staf = TSTAFF.getSelectedItem();
            String status = TSTATUS_MOHON2.getText();

            con.createStatement().executeUpdate("update penggunaan set "
                + "id_peminjaman=       '"+idpinjam+"', "
                + "kelas=               '"+kls     +"', "
                + "kegiatan=            '"+praktik +"', "
                + "tgl_pelaksanaan=     '"+tanggal1+"', "
                + "nm_barang=           '"+nama    +"', "
                + "jumlah=              '"+jml     +"', "
                + "jenis=               '"+JENIS   +"', "
                + "status_barang=       '"+stat    +"', "
                + "staff=               '"+staf    +"', "
                + "status_permohonan=   '"+status  +"' "
                + "where id_penggunaan= '"+model2.getValueAt(tabel_penggunaan.getSelectedRow(),0)+"'");
            JOptionPane.showMessageDialog(this, "Data Berhasil Di Edit");
        }catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "Data Gagal Di Edit");
        }
        clear();
        tabelPenggunaan();
        BtnHapus.setEnabled(false);
        BtnUpdate.setEnabled(false);
    }//GEN-LAST:event_BtnUpdateActionPerformed

