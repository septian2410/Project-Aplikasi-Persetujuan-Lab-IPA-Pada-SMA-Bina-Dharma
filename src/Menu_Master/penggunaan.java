        private void tabelBarang() {
            String[] judul = {"Kode Barang","Nama","Jenis Barang","Tgl Input","Jumlah","Penyimpanan","Status Barang"};
            model1 = new DefaultTableModel(judul,0);
            tabel_barang.setModel(model1);
            String sql = "SELECT * FROM barang where kd_barang like '%"+cari1.getText()+"%' or nm_barang like '%"+cari1.getText()+"%'";
        try {
            rs = con.createStatement().executeQuery(sql);
            while(rs.next()) {
               String kode = rs.getString("kd_barang");
               String nama = rs.getString("nm_barang");
               String jenis = rs.getString("jenis");
               String tgl = rs.getString("tgl_input");
               String jml = rs.getString("jumlah");
               String rak = rs.getString("penyimpanan");
               String STAT = rs.getString("status");
               String[] data = {kode,nama,jenis,tgl,jml,rak,STAT};
               model1.addRow(data);
           }
        }catch(Exception e) {
           System.out.println(e);
        }
    }
    
